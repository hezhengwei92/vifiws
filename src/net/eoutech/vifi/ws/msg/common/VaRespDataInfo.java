package net.eoutech.vifi.ws.msg.common;

public class VaRespDataInfo {

	private Integer dataType;
	private String dataValue;
	private Integer dataTT;
	private Integer dataTM;

	public Integer getDataType() {
		return dataType;
	}

	public void setDataType(Integer dataType) {
		this.dataType = dataType;
	}

	public String getDataValue() {
		return dataValue;
	}

	public void setDataValue(String dataValue) {
		this.dataValue = dataValue;
	}

	public Integer getDataTT() {
		return dataTT;
	}

	public void setDataTT(Integer dataTT) {
		this.dataTT = dataTT;
	}

	public Integer getDataTM() {
		return dataTM;
	}

	public void setDataTM(Integer dataTM) {
		this.dataTM = dataTM;
	}

}
